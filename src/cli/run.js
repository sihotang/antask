import { colorful } from 'colorful';
import { args, help, on, parse } from 'commander';
import { start } from 'gulp';
import { Logger } from "../utils";

colorful();

on('--help', () => {
  Logger.stats("  Usage:".to.bold.blue.color);
});

parse(process.argv);

const task = args[0];

if (!task) {
  help();
} else {
  Logger.stats("task run", task);

  require('../gulpfile');

  start(task);
}
